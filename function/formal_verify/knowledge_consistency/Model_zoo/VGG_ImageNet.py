    model.load_state_dict(model_zoo.load_url(model_urls['vgg16_bn'], model_dir='./pretrain-model'))
    ##############
    part1 = model.features[:train_layer]
    part2 = model.features[train_layer:]
    model.features = part1
    for param in model.parameters():
        param.requires_grad = False

    classifier = nn.Sequential(
        nn.Linear(512 * 7 * 7, 4096),
        nn.ReLU(True),
        nn.Dropout(),
        nn.Linear(4096, 4096),
        nn.ReLU(True),
        nn.Dropout(),
        nn.Linear(4096, kwargs['num_classes']),
    )

    _initialize_weights(part2)
    _initialize_weights(classifier)
    model.features = nn.Sequential(*model.features,*part2)
    model.classifier = classifier
    return model

